      where ss_quantity between 11 and 15
        and (ss_list_price between 6 and 6+10
          or ss_coupon_amt between 9594 and 9594+1000
          or ss_wholesale_cost between 10 and 10+20)) B3,
     (select avg(ss_list_price) B4_LP
